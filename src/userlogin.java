  class LoginJob implements Runnable {

        @Override
        public void run() {
            String email = jTextField1.getText();
            String password = jPasswordField1.getText();
            try {
                HttpResponse<String> httpResponse = Unirest.get("http://"+globalvariable.host+"/userLogin")
                        .queryString("email", email)
                        .queryString("password", password)
                        .asString();

                String s;
                s = (String) (httpResponse.getBody());
                if (s.equals("success")) {
                    JOptionPane.showMessageDialog(userlogin.this, "Login Successful!!");
                    dispose();
                    welcomeuser obj = new welcomeuser(email);
                    obj.setVisible(true);
                            
                 
                } else {
                    JOptionPane.showMessageDialog(userlogin.this, "Login Failed!!!");
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }
